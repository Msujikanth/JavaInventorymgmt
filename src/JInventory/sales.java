    private void sales()
    {
        String name = txtprodcode.getText();     
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con1 = DriverManager.getConnection("jdbc:mysql://localhost/java project","root","");
            insert = con1.prepareStatement("select * from product where barcode=?");
            insert.setString(1, name);//user passes the barcode its passes to the name varible name variable is set as 1 then its passes to the ?
            rs = insert.executeQuery();
            
            while (rs.next()) 
            {
                int currentqty;
                currentqty = rs.getInt("qty");//qty from DB and store
                int price = Integer.parseInt(txtprice.getText());
                int qtynew = Integer.parseInt(txtqty.getText());//assign to txtbox
                
                int tot = price*qtynew;
                
                if (qtynew >= currentqty) 
                {
                    JOptionPane.showMessageDialog(this, "Available Quantity"+" = " +currentqty);
                    JOptionPane.showMessageDialog(this, "Not Enough Quantity");
                }
                else
                {
                    model = (DefaultTableModel)jTable1.getModel();//get values to the grid
                    model.addRow(new Object[]
                    {
                        txtprodcode.getText(),
                        txtprodname.getText(),
                        txtprice.getText(),
                        txtqty.getText(),
                        tot,
                    });
                    
                    int sum=0;
                    for(int i=0; i<jTable1.getRowCount(); i++) 
                    {
                        sum = sum + Integer.parseInt(jTable1.getValueAt(i, 4).toString());
                    }
                    
                    txtsubtot.setText(Integer.toString(sum));
                    
                    txtprodcode.setText("");
                    txtprodname.setText("");
                    txtprice.setText("");
                    txtqty.setText("");
                    txtprodcode.requestFocus();
                }
            }
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(sales.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(sales.class.getName()).log(Level.SEVERE, null, ex);
        }
                
    }
    
