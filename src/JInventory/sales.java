    private void txtprodcodeKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtprodcodeKeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode()==KeyEvent.VK_ENTER) //selected enter key as the key to be pressed
        {
            String name = txtprodcode.getText();
            try {                
                Class.forName("com.mysql.jdbc.Driver");
                con1 = DriverManager.getConnection("jdbc:mysql://localhost/java project","root","");
                insert = con1.prepareStatement("select * from product where barcode=?");
                insert.setString(1, name);//user passes the barcode its passes to the name varible name variable is set as 1 then its passes to the ?
                rs = insert.executeQuery();
                
                if (rs.next()==false) 
                {
                    JOptionPane.showMessageDialog(this, "Barcode Not Found");
                }
                else
                {
                    String productname = rs.getString("product");
                    String price = rs.getString("retail_price");
                    
                    txtprodname.setText(productname.trim());
                    txtprice.setText(price.trim());
                    txtqty.requestFocus();
                }
                
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(sales.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(sales.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
    }//GEN-LAST:event_txtprodcodeKeyPressed

